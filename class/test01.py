상품명   = "밀크커피"
상품가격 = 1000
상품갯수 = 100
총금액   = 0
잔여금   = 0

화폐 = {
      '1' : 100,
      '2' : 500,
      '3' : 1000,
      '4' : 5000,
      '5' : 10000
      }
